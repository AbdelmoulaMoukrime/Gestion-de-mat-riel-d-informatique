 private void supprimerLigneDeBaseDeDonnees(String id) {
    String url = "jdbc:mysql://localhost:3306/gestionm";
    String user = "root";
    String pass = "";
    String sql = "DELETE FROM fournisseur WHERE nom = ?"; // Remplacez 'nom' par le nom de la colonne contenant l'identifiant
    
    try (Connection con = DriverManager.getConnection(url, user, pass);
         PreparedStatement pstmt = con.prepareStatement(sql)) {
        
        // Remplir les valeurs des paramètres de la requête
        pstmt.setString(1, id);
        
        // Exécuter la requête de suppression
        int rowsDeleted = pstmt.executeUpdate();
        if (rowsDeleted > 0) {
            System.out.println("Ligne supprimée de la base de données avec succès !");
        } else {
            System.out.println("Aucune ligne n'a été supprimée de la base de données !");
        }
        
    } catch (SQLException ex) {
        System.out.println("Erreur lors de la suppression de la ligne de la base de données : " + ex.getMessage());
    }
}
