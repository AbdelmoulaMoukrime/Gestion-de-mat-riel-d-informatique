     private void insererDonnees(String nom, String adresse, String email, String telephone, String site_web) {
    String url = "jdbc:mysql://localhost:3306/gestionm";
    String user = "root";
    String pass = "";
    String sql = "INSERT INTO fournisseur (nom, adresse, email, telephone, site_web) VALUES (?, ?, ?, ?, ?)";

    try (Connection con = DriverManager.getConnection(url, user, pass);
         PreparedStatement pstmt = con.prepareStatement(sql)) {

        // Remplir les valeurs des paramètres de la requête
        pstmt.setString(1, nom);
        pstmt.setString(2, adresse);
        pstmt.setString(3, email);
        pstmt.setString(4, telephone);
        pstmt.setString(5, site_web);

        // Exécuter la requête
       int rowsInserted = pstmt.executeUpdate();
        if (rowsInserted > 0) {
            JOptionPane.showMessageDialog(this, "Données ajoutées avec succès !");
            
            // Mettre à jour le modèle de tableau pour afficher les nouvelles données
            DefaultTableModel model = (DefaultTableModel) table.getModel();
            // Ajouter une nouvelle ligne avec les données insérées
            model.addRow(new Object[]{nom, adresse, email, telephone, site_web});
        }
    } catch (SQLException ex) {
        System.out.println("Erreur lors de l'insertion des données : " + ex.getMessage());
    }
    }
    
    
