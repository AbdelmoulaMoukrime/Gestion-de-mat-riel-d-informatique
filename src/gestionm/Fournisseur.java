    private void deletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtnActionPerformed
        // TODO add your handling code here:
         DefaultTableModel tblModel = (DefaultTableModel) table.getModel();
    
    if (table.getSelectedRowCount() == 1) {
        // Récupérer l'indice de la ligne sélectionnée
        int selectedRow = table.getSelectedRow();
        // Récupérer l'identifiant de la ligne sélectionnée (par exemple, la première colonne)
        String id = tblModel.getValueAt(selectedRow, 0).toString(); // Assurez-vous que la première colonne contient l'identifiant
        
        // Supprimer la ligne de la base de données correspondant à l'identifiant
        supprimerLigneDeBaseDeDonnees(id);
        
        // Supprimer la ligne du modèle de tableau
        tblModel.removeRow(selectedRow);
    } else {
        if (table.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "La table est vide !");
        } else {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner une ligne à supprimer !");
        }
    }
        
    }//GEN-LAST:event_deletebtnActionPerformed

